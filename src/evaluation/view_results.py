"""
SignalFlowAI Evaluation Viewer

Prints a side-by-side human-readable comparison of:
  - Question (generated by GPT-4o-mini)
  - Ground truth answer (generated by GPT-4o-mini QA builder)
  - Pipeline answer (generated by GPT-4o Reasoning Agent)
  - Llama-3.3-70b judge scores for each metric

Run:
    python src/evaluation/view_results.py

Optional filters:
    python src/evaluation/view_results.py --category electronics
    python src/evaluation/view_results.py --complaint_type damage_defect
    python src/evaluation/view_results.py --low_correctness     # show only score < 0.7
"""

from __future__ import annotations

import csv
import sys
import textwrap
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
DATA_DIR = ROOT / "src" / "evaluation" / "data"
DETAILS_PATH = DATA_DIR / "eval_details.csv"
SUMMARY_PATH = DATA_DIR / "eval_summary.csv"

WIDTH = 100
DIVIDER = "═" * WIDTH
THIN = "─" * WIDTH


def wrap(text: str, indent: int = 4, width: int = WIDTH - 6) -> str:
    if not text:
        return "    (empty)"
    lines = text.strip().splitlines()
    wrapped = []
    prefix = " " * indent
    for line in lines:
        if line.strip() == "":
            wrapped.append("")
        else:
            wrapped.extend(
                textwrap.wrap(line, width=width, initial_indent=prefix, subsequent_indent=prefix)
            )
    return "\n".join(wrapped)


def score_bar(score, width: int = 20) -> str:
    if score is None or score == "":
        return "[?]"
    try:
        v = float(score)
        filled = round(v * width)
        bar = "█" * filled + "░" * (width - filled)
        return f"[{bar}] {v:.2f}"
    except Exception:
        return f"[{score}]"


def confidence_label(conf: str) -> str:
    colors = {"High": "✅ High", "Medium": "⚠️  Medium", "Low": "❌ Low"}
    return colors.get(conf, conf or "-")


def load_details() -> list[dict]:
    if not DETAILS_PATH.exists():
        print(f"ERROR: {DETAILS_PATH} not found. Run: python src/evaluation/run_eval.py first.")
        sys.exit(1)
    with open(DETAILS_PATH, encoding="utf-8") as f:
        return list(csv.DictReader(f))


def load_summary() -> dict:
    if not SUMMARY_PATH.exists():
        return {}
    with open(SUMMARY_PATH, encoding="utf-8") as f:
        rows = list(csv.DictReader(f))
        return rows[0] if rows else {}


def print_summary(summary: dict) -> None:
    print(DIVIDER)
    print("  SIGNALFLOWAI - EVALUATION SUMMARY")
    print(DIVIDER)
    print(f"  Total questions  : {summary.get('total_questions', '?')}")
    print(f"  Evaluated        : {summary.get('evaluated', '?')}")
    print(f"  Failed           : {summary.get('failed', '?')}")
    print()
    print(f"  Retrieval Relevance  : {score_bar(summary.get('avg_retrieval_relevance'))}")
    print(f"  Answerability        : {score_bar(summary.get('avg_answerability'))}")
    print(f"  Answer Correctness   : {score_bar(summary.get('avg_answer_correctness'))}")
    print(f"  Faithfulness         : {score_bar(summary.get('avg_faithfulness'))}")
    print()
    print(f"  Judge model      : {summary.get('judge_model', '?')}")
    print(f"  Pipeline model   : {summary.get('pipeline_model', '?')}")
    print(f"  Retrieval backend: {summary.get('retrieval_backend', '?')}")
    print(DIVIDER)
    print()


def print_question(row: dict, index: int, total: int) -> None:
    q_id = row.get("question_id", f"q_{index}")
    category = row.get("category", "")
    complaint_type = row.get("complaint_type", "")
    question = row.get("question", "")

    r_rel = row.get("retrieval_relevance", "")
    ans = row.get("answerability", "")
    corr = row.get("answer_correctness", "")
    faith = row.get("faithfulness", "")
    conf = row.get("confidence_raw", "")

    ground_truth = row.get("ground_truth_answer", "")
    intent = row.get("pipeline_interpreted_intent", "")
    retrieval_notes = row.get("pipeline_retrieval_notes", "")
    pipeline_answer = row.get("pipeline_answer", "")
    verification = row.get("pipeline_verification", "")

    print(DIVIDER)
    print(f"  [{index}/{total}]  {q_id}  |  {category}  →  {complaint_type}")
    print(THIN)
    print(f"  QUESTION (GPT-4o-mini generated):")
    print(wrap(question, indent=4))
    print()

    print(THIN)
    print("  SCORES (Llama-3.3-70b judge):")
    print(f"    Retrieval Relevance : {score_bar(r_rel)}")
    print(f"    Answerability       : {score_bar(ans)}")
    print(f"    Answer Correctness  : {score_bar(corr)}")
    print(f"    Faithfulness        : {score_bar(faith)}")
    print(f"    Verifier Confidence : {confidence_label(conf)}")
    print()

    print(THIN)
    print("  GROUND TRUTH ANSWER (GPT-4o-mini, from QA generator):")
    print(wrap(ground_truth, indent=4))
    print()

    print(THIN)
    print("  PIPELINE - Query Agent interpreted intent:")
    print(wrap(intent, indent=4))
    print()
    print("  PIPELINE - Retrieval Agent notes:")
    print(wrap(retrieval_notes, indent=4))
    print()
    print("  PIPELINE - Reasoning Agent answer (GPT-4o):")
    print(wrap(pipeline_answer, indent=4))
    print()
    print("  PIPELINE - Verifier Agent:")
    print(wrap(verification, indent=4))
    print()


def main() -> None:
    args = sys.argv[1:]
    filter_category = None
    filter_complaint = None
    only_low_correctness = False

    i = 0
    while i < len(args):
        if args[i] == "--category" and i + 1 < len(args):
            filter_category = args[i + 1]
            i += 2
        elif args[i] == "--complaint_type" and i + 1 < len(args):
            filter_complaint = args[i + 1]
            i += 2
        elif args[i] == "--low_correctness":
            only_low_correctness = True
            i += 1
        else:
            i += 1

    rows = load_details()
    summary = load_summary()

    # Apply filters
    filtered = []
    for r in rows:
        if filter_category and r.get("category", "") != filter_category:
            continue
        if filter_complaint and r.get("complaint_type", "") != filter_complaint:
            continue
        if only_low_correctness:
            try:
                if float(r.get("answer_correctness", 1.0)) >= 0.7:
                    continue
            except Exception:
                pass
        filtered.append(r)

    print_summary(summary)

    if not filtered:
        print("No results match the given filters.")
        return

    print(f"  Showing {len(filtered)} question(s)\n")

    for idx, row in enumerate(filtered, 1):
        print_question(row, idx, len(filtered))

    print(DIVIDER)
    print(f"  End of evaluation report - {len(filtered)} question(s) shown")
    print(DIVIDER)


if __name__ == "__main__":
    main()
